# -*- coding: cp1251 -*-
from app import app
from models import db, Book, Genre, Cover

with app.app_context():
    print("Начало добавления тестовых книг...")

    # Проверяем, есть ли уже книги, чтобы не дублировать
    if Book.query.first():
        print("Книги уже есть в базе данных.")
        exit()

    # Получаем жанры из базы, чтобы связать их с книгами
    fantasika = Genre.query.get(1)       # Фантастика
    roman = Genre.query.get(2)            # Роман
    detektiv = Genre.query.get(3)         # Детектив
    fantasy = Genre.query.get(4)          # Фэнтези
    science = Genre.query.get(5)          # Научная литература
    classic = Genre.query.get(6)          # Классика

    # Книга 1: Фантастика / Научная литература
    book1 = Book(
        title="Интерстеллар: Наука за кулисами",
        author="Кип Торн",
        year=2015,
        publisher="Манн, Иванов и Фербер",
        pages=336,
        description=(
            "# Описание научной основы фильма\n\n"
            "Известный физик-теоретик **Кип Торн** подробно описывает научные гипотезы и открытия, "
            "которые легли в основу знаменитого научно-фантастического фильма Кристофера Нолана.\n\n"
            "### Основные темы книги:\n"
            "* Природа гравитационных аномалий и черных дыр;\n"
            "* Моделирование супермассивной черной дыры *Гаргантюа*;\n"
            "* Пятое измерение, кротовые норы и замедление времени.\n\n"
            "Рекомендуется всем любителям качественной твердой научной фантастики и астрофизики!"
        )
    )
    if fantasika: book1.genres.append(fantasika)
    if science: book1.genres.append(science)

    # Книга 2: Классика / Роман
    book2 = Book(
        title="Мастер и Маргарита",
        author="Михаил Булгаков",
        year=1967,
        publisher="Художественная литература",
        pages=480,
        description=(
            "# Бессмертный шедевр русской литературы\n\n"
            "Удивительный роман, сочетающий в себе философию, сатиру, мистику и любовную драму. "
            "Действие разворачивается в Москве 1930-х годов, которую посещает загадочный иностранец Воланд со своей свитой.\n\n"
            "### Ключевые сюжетные линии:\n"
            "1. **Воланд в Москве** — сатирическое обличение советского общества.\n"
            "2. **История Мастера и Маргариты** — великая и преданная любовь.\n"
            "3. **Понтий Пилат и Иешуа** — философские главы романа.\n\n"
            "*Рукописи, как известно, не горят!*"
        )
    )
    if classic: book2.genres.append(classic)
    if roman: book2.genres.append(roman)

    # Книга 3: Детектив
    book3 = Book(
        title="Шерлок Холмс: Этюд в багровых тонах",
        author="Артур Конан Дойл",
        year=1887,
        publisher="Ward Lock & Co",
        pages=220,
        description=(
            "# Первое появление легендарного сыщика\n\n"
            "Повесть, в которой доктор Джон Ватсон знакомится с эксцентричным мистером Шерлоком Холмсом. "
            "Им предстоит расследовать загадочное убийство в заброшенном доме в Лондоне.\n\n"
            "### Особенности дедуктивного метода Холмса:\n"
            "* Наблюдение за мельчайшими деталями внешности;\n"
            "* Глубокие знания химии и криминалистики;\n"
            "* Логическое исключение невозможных вариантов.\n\n"
            "Именно здесь зарождается самый знаменитый дуэт в истории детективного жанра."
        )
    )
    if detektiv: book3.genres.append(detektiv)

    # Добавляем книги в сессию
    db.session.add_all([book1, book2, book3])
    db.session.flush()

    # Создаем для них заглушки обложек (но на самом деле файлы картинок нужно положить в static/uploads/)
    cover1 = Cover(filename="interstellar.jpg", mime_type="image/jpeg", md5_hash="hash1", book_id=book1.id)
    cover2 = Cover(filename="master.jpg", mime_type="image/jpeg", md5_hash="hash2", book_id=book2.id)
    cover3 = Cover(filename="sherlock.jpg", mime_type="image/jpeg", md5_hash="hash3", book_id=book3.id)
    
    db.session.add_all([cover1, cover2, cover3])
    
    db.session.commit()
    print("3 демонстрационные книги успешно добавлены в СУБД SQLite!")